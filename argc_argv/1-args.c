#include "main.h"
#include <stdio.h>
/**
 * main - gets amount of arguments passed into it
 * @argc: count of arguments
 *
 * Return: number of arguments
 */
int main(int argc, char *argv[])
{
	int i;
	
	for (i = 0; i < argc; i++)
	{
		continue;
	}
		printf("%d", i);
		printf("\n");

	return (0);
}
